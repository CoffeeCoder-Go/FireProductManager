    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        
        produto.setNome(nome.getText());
        produto.setQuantidade(Integer.valueOf(quantidade.getText()));
        produto.setPrecoUnidade(Float.valueOf(preco.getText()));
        
        this.controller.update(produto.getId(), produto);
    }//GEN-LAST:event_saveActionPerformed

    private void minusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_minusActionPerformed
        // TODO add your handling code here:
        int number = Integer.parseInt(quantidade.getText());
        
        if(number > 0) --number;
        
        quantidade.setText(Integer.toString(number));
    }//GEN-LAST:event_minusActionPerformed

    private void plusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_plusActionPerformed
        // TODO add your handling code here:
        int number = Integer.parseInt(quantidade.getText());
        
        ++number;
        
        quantidade.setText(Integer.toString(number));
    }//GEN-LAST:event_plusActionPerformed

    private void deletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletarActionPerformed
        // TODO add your handling code here:
        controller.delete(produto.getId());
        this.dispose();
    }//GEN-LAST:event_deletarActionPerformed

